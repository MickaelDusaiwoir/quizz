    <h2>
        Objectif Développement Durable - Exploratemporium
    </h2>
    <p>
        Embarquer à bord de l'Exploratemporium, une machine à voyager dans le temps,
        afin d'aider le savant fou Théodore Artémus à enquêter sur l'évolution de notre empreinte énergétique au cours du temps...
        Telle est la mission des enfants qui viennent visiter l'exposition.
        A la fois acteurs et spectateurs, ils doivent trouver les éléments de réponse à leur mission via des vidéos interactives, des enquêtes,
        des manipulations et un forum final.        
    </p>
    <p>
        Cette exposition se veut un véritable outil de vulgarisation afin non seulement d'aborder les notions de développement durable et d'empreinte énergétique,
        mais surtout de porter un regard sur notre manière de vivre et penser aux petites gestes quotidiens et solutions pour l'avenir.
        Installée sous un chapiteau occulté de 150m²,
        l'exposition a été conçue pour l'itinérance avec le souci de réduire au maximum son empreinte énergétique en utilisant les technologies appropriées pour l'isolation thermique,
        la consommation électrique, etc. Une réflexion en ce sens a été menée avec la collaboration, entre autres,
        du Service provincial d'information sur l'environnement et l'énergie, et de l'Université de Liège.
    </p>
